    private void editButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editButtonMouseClicked
        // TODO add your handling code here:
        String s1,s2;
        int pos1 = nodesTable.getSelectedRow();
        int pos2 = nodesTable2.getSelectedRow();
        if (pos1 != -1 && pos2 != -1){
            DefaultTableModel model =  (DefaultTableModel) nodesTable.getModel();
            DefaultTableModel model2 =  (DefaultTableModel) nodesTable2.getModel();
            
            Object valueAt = model.getValueAt(pos1, 0);
            Object valueAt2 = model2.getValueAt(pos2, 0);
            
            s1 = (String) valueAt;
            s2 = (String) valueAt2;
            
            s1 = s1.trim();
            s2 = s2.trim();
            
            if (!(s1.equals("") || s2.equals(""))){
                System.out.println("s1: "+s1+"\ns2: "+s2);
                vc.modifySolution(s1,s2);
                actTables();
            }
            else JOptionPane.showConfirmDialog(null, "2 rows must be selected", "ERROR:", JOptionPane.OK_CANCEL_OPTION);
        }
        else JOptionPane.showConfirmDialog(null, "2 rows must be selected", "ERROR:", JOptionPane.OK_CANCEL_OPTION);
    }//GEN-LAST:event_editButtonMouseClicked


