    private void actTables(){
        DefaultTableModel model =  (DefaultTableModel) nodesTable.getModel();
        DefaultTableModel model2 =  (DefaultTableModel) nodesTable2.getModel();
        while(model.getRowCount()>0)model.removeRow(0);
        while(model2.getRowCount()>0)model2.removeRow(0);
        
        for (int i = 0; i < vc.getSolutionString().size(); i++) {
            model.addRow(new Object[] {vc.getSolutionString().get(i)});
            model2.addRow(new Object[] {vc.getSolutionString().get(i)});
        }
    }

